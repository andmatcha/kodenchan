// CANフィルタ設定: ID 0x208のみ受信
void CAN_FilterConfig(void) {
  can_filter.FilterBank = 0;
  can_filter.FilterMode = CAN_FILTERMODE_IDMASK;
  can_filter.FilterScale = CAN_FILTERSCALE_32BIT;
  can_filter.FilterIdHigh = (0x208 << 5);
  can_filter.FilterIdLow = 0x0000;
  can_filter.FilterMaskIdHigh = (0x7FF << 5);
  can_filter.FilterMaskIdLow = 0x0000;
  can_filter.FilterFIFOAssignment = CAN_RX_FIFO0;
  can_filter.FilterActivation = CAN_FILTER_ENABLE;
  can_filter.SlaveStartFilterBank = 14;

  HAL_CAN_ConfigFilter(&hcan, &can_filter);
}

// サーボモーターの角度を設定 (0-270度)
void Servo_SetAngle(float angle) {
  if (angle < 0.0f) angle = 0.0f;
  if (angle > SERVO_ANGLE_MAX) angle = SERVO_ANGLE_MAX;

  uint32_t pulse = SERVO_MIN_PULSE + (uint32_t)((angle / SERVO_ANGLE_MAX) * (SERVO_MAX_PULSE - SERVO_MIN_PULSE));
  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, pulse);
}

// 角度を更新 (タイマー割り込みから呼ばれる)
void Servo_UpdateAngle(void) {
  if (servo_command == 0) {
    // 角度を増やす (270度方向)
    servo_current_angle += SERVO_SPEED;
    if (servo_current_angle > SERVO_ANGLE_MAX) {
      servo_current_angle = SERVO_ANGLE_MAX;
    }
    Servo_SetAngle(servo_current_angle);
  } else if (servo_command == 2) {
    // 角度を減らす (0度方向)
    servo_current_angle -= SERVO_SPEED;
    if (servo_current_angle < 0.0f) {
      servo_current_angle = 0.0f;
    }
    Servo_SetAngle(servo_current_angle);
  }
  // servo_command == 1 の場合は停止 (何もしない)
}

// CAN RX割り込みハンドラ
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan_param) {
  if (HAL_CAN_GetRxMessage(hcan_param, CAN_RX_FIFO0, &rx_header, rx_data) == HAL_OK) {
    // ID 0x208のメッセージを受信
    if (rx_header.StdId == 0x208) {
      // rx_data[2]でサーボモーターの動作を制御
      if (rx_data[2] <= 2) {
        servo_command = rx_data[2];
      }
    }
  }
  printf("Received CAN ID: 0x%03X Data: %02X %02X\r\n",rx_header.StdId, rx_data[2]);
}

// TIM1割り込みハンドラ (20ms周期で角度を更新)
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
  if (htim->Instance == TIM1) {
    Servo_UpdateAngle();
  }
}

